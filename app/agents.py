def fallback_threads_post(topic: str) -> str:
    # Small Ollama models can produce low-quality Threads drafts, so Threads generation is fallback-first.
    topic = _clean_topic(topic)
    if has_forbidden_post_phrase(topic):
        topic = "обработки заявок"
    if is_clinic_topic(topic):
        return _with_threads_cta(CLINIC_THREADS_FALLBACK_BODY)

    body = (
        f"В теме «{topic}» бизнес теряет деньги не из-за рекламы, а из-за медленного ответа.\n\n"
        "Клиент пишет сразу нескольким компаниям. Пока администратор ищет диалог, "
        "оплаченная заявка уходит конкуренту и не попадает в CRM.\n\n"
        "AI-бот отвечает сразу, уточняет запрос и передаёт тёплый контакт менеджеру."
    )
    return _with_threads_cta(body)


def safe_threads_post(topic: str, generated_text: str | None = None) -> str:
    text = (generated_text or "").strip()
    if 180 <= len(text) <= 380 and not has_forbidden_post_phrase(text):
        candidate = _with_threads_cta(text)
        if len(candidate) <= 450:
            return candidate
    return fallback_threads_post(topic)


