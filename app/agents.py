AGENTS = {c: Agent(c,n,b,d,STYLE+"\n"+s,t,e) for c,n,b,d,s,t,e in _defs}

def build_prompt(command: str, text: str) -> str:
    a = AGENTS[command]
    return f"{a.system_instructions}\n\nЗадача: {a.user_template.format(input=text or 'не указан')}"

def agents_help() -> str:
    blocks = []
    for block in ["Маркетинг и контент","Продажи и диалоги","Аналитика, офферы и КП","Разработка, ТЗ и проект"]:
        lines = [f"{block}:"]
        for i,a in enumerate([x for x in AGENTS.values() if x.block == block], 1):
            num = list(AGENTS).index(a.command)+1
            lines.append(f"{num}. {a.command} — {a.description}")
        blocks.append("\n".join(lines))
    blocks.append("Threads Publishing Agent:\n• /viral_threads_day\n• /viral_post ниша\n• /positioning\n• /threads_day\n• /threads_post тема поста\n• /threads_queue\n• /threads_publish id_поста\n• /threads_rewrite id_поста\n• /threads_skip id_поста\n• /threads_next\n• /growth_status — статус Threads Growth Agent\n• /growth_refill — пополнить очередь сильными постами\n• /growth_plan — план раскачки на день\n• /engagement_tasks — ручные задания для роста охвата")
    return "\n\n".join(blocks)
