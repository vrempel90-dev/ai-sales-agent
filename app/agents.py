def is_good_generated_post(text: str, topic: str) -> bool:
    normalized = " ".join((text or "").lower().split())
    if len(normalized) < 120 or len(normalized) > 900:
        return False
    if any(phrase in normalized for phrase in FORBIDDEN_POST_PHRASES):
        return False
    if "ai" not in normalized and "бот" not in normalized:
        return False
    if not any(word in normalized for word in ("заяв", "клиент", "crm", "follow-up", "админ", "продаж", "запис")):
        return False
    topic_words = [word.lower() for word in topic.replace("24/7", "24").split() if len(word) > 3]
    if not any(word in normalized for word in topic_words):
        return False
    return True



def _clean_topic(topic: str) -> str:
    normalized = " ".join((topic or "").strip().split())
    return normalized or "AI-бот для бизнеса"


def is_clinic_topic(topic: str) -> bool:
    normalized = _clean_topic(topic).lower()
    return any(keyword in normalized for keyword in CLINIC_TOPIC_KEYWORDS)


def has_forbidden_post_phrase(text: str) -> bool:
    normalized = " ".join((text or "").lower().split())
    return any(phrase in normalized for phrase in FORBIDDEN_POST_PHRASES)

